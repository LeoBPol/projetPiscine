// Fonctions onmouseover/out pour "Aide"

function onUnderlinedText(obj)
{
    obj.classList.add('OMOUnderline')
}

function outUnderlinedText(obj)
{
    obj.classList.remove('OMOUnderline')
}

// Fonctions onmouseover/out pour le footer

function onColoredText(obj)
{
    obj.classList.add('OMOColor')
}

function outColoredText(obj)
{
    obj.classList.remove('OMOColor')
}